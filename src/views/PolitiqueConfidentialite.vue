3. Vos droits concernant vos données personnelles

Le propriétaire des données a les droits suivants :

Droit à l’information : vous avez le droit de savoir comment sont traitées vos données personnelles ; quelles sont les données recueillies, comment sont-elles obtenues, pourquoi et qui les traite.
Droit d’accès : vous avez le droit d’accéder aux données collectées vous concernant. Cela inclus le droit de demander et d’obtenir une copie de vos données personnelles recueillies. 

Droit de rectification : vous avez le droit de demander la rectification ou la suppression de vos données personnelles si celles-ci sont inexactes ou incomplètes. 

Droit d’effacement : dans certaines circonstances, vous pouvez demander à ce que vos données personnelles soient effacées de notre base de données.
Droit de limiter le traitement : lorsque certaines conditions sont remplies, vous avez le droit de limiter le traitement de vos données personnelles.

Droit d’opposition au traitement : dans certaines situations, vous avez le droit de vous opposer au traitement de vos données personnelles, c’est la cas pour le marketing direct.

Droit d’opposition au traitement automatique : vous avez le droit de vous opposer au traitement automatique, incluant le profilage. Vous pouvez exercer ce droit à chaque fois que le profilage aboutit à des effets légaux vous concernant ou vous affectant significativement.
Droit à la portabilité des données : vous avez le droit d’obtenir vos données personnelles au format “machine-readable”, c’est à dire, sous forme d’un fichier informatique.

Droit de plainte : dans le cas où nous refuserions votre demande d’accès à vos données personnelles, nous vous fournirions une raison à ce refus. Si vous n’étiez pas satisfait de la façon dont votre demande aura été traitée, merci de nous contacter.

Droit à l’assistance d’une autorité de contrôle : vous avez le droit d’être assisté par une autorité de contrôle et le droit à d’autres recours légaux comme la demande de dommages et intérêts.
Droit de retrait de consentement : vous avez le droit de retirer votre consentement pour le traitement de vos données personnelles.

